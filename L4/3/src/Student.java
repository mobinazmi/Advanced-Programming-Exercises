public class Student <a,b,c>{
    private a Name;
    private b StudentNumber;
    private c Father;

    public a getName() {
        return Name;
    }

    public void setName(a name) {
        Name = name;
    }

    public b getStudentNumber() {
        return StudentNumber;
    }

    public void setStudentNumber(b studentNumber) {
        StudentNumber = studentNumber;
    }

    public c getFather() {
        return Father;
    }

    public void setFather(c father) {
        Father = father;
    }
}
